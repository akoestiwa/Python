# Operasi boolean

print('== NOT ==')
a = True
hasil = not a
print('a =',a)
print('a not =',hasil)

print('\n== OR ==')
a = True
b = False
hasil = a or b
print('a =',a)
print('b =',b)
print('a or b =',hasil)

print('\n== AND ==')
a = True
b = False
hasil = a and b
print('a =',a)
print('b =',b)
print('a and b =',hasil)

print('\n== XOR ==')
a = True
b = False
hasil = a ^ b
print('a =',a)
print('b =',b)
print('a ^ b =',hasil)